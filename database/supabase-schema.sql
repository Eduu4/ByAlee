-- Activa eventos en tiempo real para que las solicitudes aparezcan sin recargar.
alter table public.appointments replica identity full;
do $$
begin
  if not exists (
    select 1
    from pg_publication_tables
    where pubname = 'supabase_realtime'
      and schemaname = 'public'
      and tablename = 'appointments'
  ) then
    alter publication supabase_realtime add table public.appointments;
  end if;
end $$;

drop policy if exists studios_member_select on public.studios;
create policy studios_member_select on public.studios for select to authenticated using (public.is_studio_member(id));

drop policy if exists profiles_own_studio on public.profiles;
create policy profiles_own_studio on public.profiles for select to authenticated using (id = auth.uid() or public.is_studio_member(studio_id));

drop policy if exists profiles_admin_update on public.profiles;
create policy profiles_admin_update on public.profiles for update to authenticated using (public.is_studio_member(studio_id)) with check (public.is_studio_member(studio_id));

-- Política común para tablas con studio_id.
do $$
declare
  table_name text;
  policy_name text;
begin
  foreach table_name in array array['studio_settings','services','clients','client_records','appointments','visits','inventory_items','availability_blocks','media_files']
  loop
    policy_name := table_name || '_member_all';
    execute format('drop policy if exists %I on public.%I', policy_name, table_name);
    execute format(
      'create policy %I on public.%I for all to authenticated using (public.is_studio_member(studio_id)) with check (public.is_studio_member(studio_id))',
      policy_name, table_name
    );
  end loop;
end $$;


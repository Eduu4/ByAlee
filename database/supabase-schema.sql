create extension if not exists pgcrypto;
create extension if not exists btree_gist;

create table if not exists public.studios (
  id uuid primary key default gen_random_uuid(),
  slug text not null unique,
  name text not null,
  timezone text not null default 'America/Asuncion',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  studio_id uuid not null references public.studios(id) on delete cascade,
  full_name text not null default 'ByAlee',
  role text not null default 'admin' check (role in ('admin','professional')),
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.studio_settings (
  studio_id uuid primary key references public.studios(id) on delete cascade,
  data jsonb not null default '{}'::jsonb,
  updated_at timestamptz not null default now()
);

create table if not exists public.services (
  id bigint generated by default as identity primary key,
  studio_id uuid not null references public.studios(id) on delete cascade,
  name text not null,
  category text not null default 'Pestañas',
  active boolean not null default true,
  data jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (studio_id, name)
);

create table if not exists public.clients (
  id bigint generated by default as identity primary key,
  studio_id uuid not null references public.studios(id) on delete cascade,
  full_name text not null,
  whatsapp text not null,
  phone_key text not null,
  birth_date date,
  birthday_marketing_consent boolean not null default false,
  data jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (studio_id, phone_key)
);

create table if not exists public.client_records (
  id bigint generated by default as identity primary key,
  studio_id uuid not null references public.studios(id) on delete cascade,
  client_id bigint not null references public.clients(id) on delete cascade,
  data jsonb not null default '{}'::jsonb,
  updated_at timestamptz not null default now(),
  unique (studio_id, client_id)
);

create table if not exists public.appointments (
  id bigint generated by default as identity primary key,
  studio_id uuid not null references public.studios(id) on delete cascade,
  client_id bigint references public.clients(id) on delete set null,
  service_id bigint not null references public.services(id),
  date date not null,
  time time not null,
  start_minute integer not null check (start_minute between 0 and 1439),
  end_minute integer not null check (end_minute between 1 and 1800 and end_minute > start_minute),
  status text not null default 'requested' check (status in ('requested','pending','confirmed','completed','canceled','rejected','no_show')),
  data jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists idx_appointments_studio_date on public.appointments(studio_id, date);
create index if not exists idx_appointments_status on public.appointments(studio_id, status);

-- Evita que dos solicitudes activas ocupen el mismo rango incluso si llegan al mismo tiempo.
do $$
begin
  if not exists (
    select 1 from pg_constraint where conname = 'appointments_no_active_overlap'
  ) then
    alter table public.appointments
      add constraint appointments_no_active_overlap
      exclude using gist (
        studio_id with =,
        date with =,
        int4range(start_minute, end_minute, '[)') with &&
      ) where (status in ('requested','pending','confirmed'));
  end if;
end $$;


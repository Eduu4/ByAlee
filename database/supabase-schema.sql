create or replace function public.touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

-- Crear triggers idempotentes.
do $$
declare
  table_name text;
  trigger_name text;
begin
  foreach table_name in array array['studios','profiles','studio_settings','services','clients','client_records','appointments','visits','inventory_items','availability_blocks']
  loop
    trigger_name := 'touch_' || table_name || '_updated_at';
    execute format('drop trigger if exists %I on public.%I', trigger_name, table_name);
    execute format('create trigger %I before update on public.%I for each row execute function public.touch_updated_at()', trigger_name, table_name);
  end loop;
end $$;

create or replace function public.is_studio_member(target_studio uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.profiles
    where id = auth.uid()
      and studio_id = target_studio
      and active = true
  );
$$;

create or replace function public.current_studio_id()
returns uuid
language sql
stable
security definer
set search_path = public
as $$
  select studio_id from public.profiles where id = auth.uid() and active = true limit 1;
$$;

grant execute on function public.is_studio_member(uuid) to authenticated;
grant execute on function public.current_studio_id() to authenticated;

grant usage on schema public to authenticated;
grant select, insert, update, delete on all tables in schema public to authenticated;
grant usage, select on all sequences in schema public to authenticated;
alter default privileges in schema public grant select, insert, update, delete on tables to authenticated;
alter default privileges in schema public grant usage, select on sequences to authenticated;

